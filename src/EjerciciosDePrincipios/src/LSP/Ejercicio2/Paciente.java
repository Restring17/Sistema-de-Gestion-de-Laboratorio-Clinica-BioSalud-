
package LSP.Ejercicio2;

public class Paciente {
    String nombre;
    String id;

    public Paciente(String nombre, String id) {
        this.nombre = nombre;
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public String getId() {
        return id;
    }
    
}
    
    

