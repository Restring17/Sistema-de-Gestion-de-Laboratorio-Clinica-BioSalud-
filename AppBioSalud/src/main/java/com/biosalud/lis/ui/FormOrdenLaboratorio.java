    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        try {
            int fila = tblOrdenLaboratorio.getSelectedRow();
            if (fila == -1) {
                JOptionPane.showMessageDialog(this, "Debe seleccionar una orden.");
                return;
            }

            int id = Integer.parseInt(tblOrdenLaboratorio.getValueAt(fila, 0).toString());

            controller.eliminarOrden(id);

            cargarTabla();
            limpiarCampos();

            JOptionPane.showMessageDialog(this, "Orden eliminada.");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al eliminar: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

