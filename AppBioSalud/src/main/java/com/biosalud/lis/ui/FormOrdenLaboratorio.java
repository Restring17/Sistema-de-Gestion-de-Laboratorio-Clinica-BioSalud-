    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            OrdenLaboratorio o = new OrdenLaboratorio();

            o.setIdPaciente(Integer.parseInt(jTxtPaciente.getText()));
            o.setIdMedico(Integer.parseInt(jTxtMedico.getText()));

            // FECHA FORMATO: yyyy-MM-dd
            java.sql.Date fecha = java.sql.Date.valueOf(jTxtFecha.getText());
            o.setFechaOrden(fecha);

            o.setTipoExamen(cmbTipoExamen.getSelectedItem().toString());
            o.setObservaciones(jTxtObservaciones.getText());
            o.setEstado("PENDIENTE");

            controller.guardarOrden(o);

            cargarTabla();
            limpiarCampos();

            JOptionPane.showMessageDialog(this, "Orden registrada correctamente.");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al guardar: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        try {
            int fila = tblOrdenLaboratorio.getSelectedRow();
            if (fila == -1) {
                JOptionPane.showMessageDialog(this, "Seleccione una orden de la tabla.");
                return;
            }

            int idOrden = Integer.parseInt(tblOrdenLaboratorio.getValueAt(fila, 0).toString());

            OrdenLaboratorio o = new OrdenLaboratorio();

            o.setIdOrden(idOrden);
            o.setIdPaciente(Integer.parseInt(jTxtPaciente.getText()));
            o.setIdMedico(Integer.parseInt(jTxtMedico.getText()));

            java.sql.Date fecha = java.sql.Date.valueOf(jTxtFecha.getText());
            o.setFechaOrden(fecha);

            o.setTipoExamen(cmbTipoExamen.getSelectedItem().toString());
            o.setObservaciones(jTxtObservaciones.getText());
            o.setEstado("Pendiente"); // o tu l√≥gica

            controller.actualizarOrden(o);

            cargarTabla();
            limpiarCampos();

            JOptionPane.showMessageDialog(this, "Orden actualizada.");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al actualizar: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

