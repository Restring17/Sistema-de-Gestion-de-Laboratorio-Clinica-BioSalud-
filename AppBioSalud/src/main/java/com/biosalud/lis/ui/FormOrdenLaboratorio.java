    private void cargarTabla() {
        try {
            List<OrdenLaboratorio> lista = controller.listarOrdenes();

            DefaultTableModel modelo = new DefaultTableModel(
                    new Object[]{"ID", "Paciente", "Medico", "Fecha", "Tipo Examen", "Estado"},
                    0
            );

            for (OrdenLaboratorio o : lista) {
                modelo.addRow(new Object[]{
                    o.getIdOrden(),
                    o.getIdPaciente(),
                    o.getIdMedico(),
                    o.getFechaOrden(),
                    o.getTipoExamen(),
                    o.getEstado()
                });
            }

            tblOrdenLaboratorio.setModel(modelo);

            // actualizar contador
            cantOrdenes.setText(String.valueOf(lista.size()));

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al cargar lista: " + ex.getMessage());
        }
    }

