    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        if (jTxtUsuario.getText().isEmpty()
                || jTxtNombre.getText().isEmpty()
                || jTxtEmail.getText().isEmpty()
                || jContraseña.getPassword().length == 0) {

            JOptionPane.showMessageDialog(this,
                    "Complete todos los campos para registrar un nuevo usuario.");
            return;
        }

        Usuario u = new Usuario();
        u.setUsername(jTxtUsuario.getText().trim());
        u.setNombre(jTxtNombre.getText().trim());
        u.setEmail(jTxtEmail.getText().trim());
        u.setRol(cmbBoxRol.getSelectedItem().toString());
        u.setEstado(cmbEstado.getSelectedItem().toString());

        boolean ok = controller.registrar(u, new String(jContraseña.getPassword()));

        if (ok) {
            JOptionPane.showMessageDialog(this, "Usuario registrado correctamente.");
            limpiarForm();
        } else {
            JOptionPane.showMessageDialog(this, "Error al registrar usuario");
        }

    }//GEN-LAST:event_btnCrearActionPerformed

